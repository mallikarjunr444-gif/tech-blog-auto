def post_email(title, html):
    print("  Posting to Blogger via email...")
    pw = os.environ.get("GMAIL_APP_PASSWORD", GMAIL_APP_PASSWORD)
    msg = MIMEMultipart("alternative")
    msg["Subject"] = title
    msg["From"]    = GMAIL_ADDRESS
    msg["To"]      = BLOGGER_POST_EMAIL
    msg.attach(MIMEText(html, "html"))
    with smtplib.SMTP_SSL("smtp.gmail.com", 465) as s:
        s.login(GMAIL_ADDRESS, pw)
        s.sendmail(GMAIL_ADDRESS, BLOGGER_POST_EMAIL, msg.as_string())
    print("  Posted! https://technewswithai.blogspot.com")
    return "https://technewswithai.blogspot.com"


def auto_labels(title, html, base):
    text = (title + " " + html[:500]).lower()
    labels = list(base)
    MAP = {
        "oppo":["OPPO"], "honor":["Honor"], "huawei":["Huawei"],
        "iphone":["iPhone","Apple"], "samsung":["Samsung"],
        "macbook":["MacBook","Apple"], "oneplus":["OnePlus"],
        "realme":["Realme"], "xiaomi":["Xiaomi"], "redmi":["Redmi"],
        "poco":["Poco"], "nothing":["Nothing Phone"],
        "pixel":["Google Pixel"], "laptop":["Laptop"],
        "vivo":["Vivo"], "iqoo":["iQOO"], "motorola":["Motorola"],
        "airpods":["AirPods"], "earbuds":["TWS Earbuds"],
        "headphone":["Headphones"], "watch":["Smartwatch"],
        "tablet":["Tablet"], "power bank":["Power Bank"],
        "india":["Price in India"], "2026":["2026"],
        "review":["Review"], "launch":["New Launch"],
        "gaming":["Gaming"],
    }
    for kw, tags in MAP.items():
        if kw in text:
            for t in tags:
                if t not in labels:
                    labels.append(t)
    return list(set(labels))[:10]


def save_log(title, url, words, cat):
    log = []
    if os.path.exists("posted_articles.json"):
        try:
            with open("posted_articles.json") as f:
                log = json.load(f)
        except Exception:
            log = []
    log.append({
        "title": title, "url": url,
        "words": words, "category": cat,
        "posted_at": datetime.datetime.now().isoformat()
    })
    with open("posted_articles.json", "w") as f:
        json.dump(log, f, indent=2)
    print("  Log saved — " + str(len(log)) + " total posts")


