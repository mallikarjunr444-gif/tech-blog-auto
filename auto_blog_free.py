def finalize(raw, story):
    raw = re.sub(r"```html", "", raw)
    raw = re.sub(r"```", "", raw)
    raw = raw.strip()

    title = story["title"]
    m = re.search(r"<h[12][^>]*>(.*?)</h[12]>", raw, re.IGNORECASE | re.DOTALL)
    if m:
        title = re.sub(r"<[^>]+>", "", m.group(1)).strip()

    words = len(re.sub(r"<[^>]+>", "", raw).split())

    footer = (
        "<hr/>"
        '<p style="font-size:12px;color:#888;"><em>'
        "Source: " + story["source"] + " &bull; "
        '<a href="' + story["url"] + '" target="_blank" rel="noopener">Read Original</a>'
        ' &bull; <a href="https://technewswithai.blogspot.com">More on Tech News With AI</a>'
        "</em></p>"
    )

    return title, raw + footer, words


