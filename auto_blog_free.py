def fetch_rss(name, url):
    try:
        r = requests.get(url, headers=HEADERS, timeout=8)
        if r.status_code != 200:
            return []
        root = ET.fromstring(r.content)
        items = root.findall(".//item")
        if not items:
            items = root.findall(".//{http://www.w3.org/2005/Atom}entry")
        results = []
        for item in items[:8]:
            t = item.find("title")
            title = t.text.strip() if t is not None and t.text else ""
            if len(title) < 20:
                continue
            d = item.find("description")
            if d is None:
                d = item.find("{http://www.w3.org/2005/Atom}summary")
            desc = ""
            if d is not None and d.text:
                desc = re.sub(r"<[^>]+>", "", d.text)[:400]
            l = item.find("link")
            link = l.text.strip() if l is not None and l.text else url
            results.append({
                "title": title,
                "description": desc,
                "url": link,
                "source": name,
                "published": datetime.datetime.now().isoformat(),
            })
        return results[:3]
    except Exception:
        return []


def fetch_newsapi(query):
    try:
        r = requests.get(
            "https://newsapi.org/v2/everything",
            params={"q": query, "language": "en",
                    "sortBy": "publishedAt", "pageSize": 5,
                    "apiKey": NEWS_API_KEY},
            timeout=10
        )
        data = r.json()
        if data.get("status") != "ok":
            return []
        return [{"title": a["title"],
                 "description": a.get("description", "") or "",
                 "url": a["url"],
                 "source": a["source"]["name"],
                 "published": a["publishedAt"]}
                for a in data.get("articles", [])
                if a.get("title") and a.get("description")]
    except Exception:
        return []


def get_specs(device):
    try:
        q = device.replace(" ", "+")
        r = requests.get(
            "https://www.gsmarena.com/search.php3?sQuickSearch=" + q,
            headers=HEADERS, timeout=8
        )
        links = re.findall(r'href="([a-z0-9_]+-\d+\.php)"', r.text)
        if not links:
            return ""
        r2 = requests.get(
            "https://www.gsmarena.com/" + links[0],
            headers=HEADERS, timeout=8
        )
        pairs = re.findall(
            r'<td class="ttl">.*?<a[^>]*>([^<]+)</a>.*?</td>\s*<td class="nfo">([^<]+)',
            r2.text, re.DOTALL
        )
        return "\n".join([k.strip() + ": " + v.strip() for k, v in pairs[:30]])
    except Exception:
        return ""


